package api.payloads;

public class Category
{
    int id;
    String name;

    public int getCategoryId()
    {
        return id;
    }

    public void setCategoryId(int id)
    {
        this.id = id;
    }

    public String getCategoryName()
    {
        return name;
    }

    public void setCategoryName(String name)
    {
        this.name = name;
    }


}
